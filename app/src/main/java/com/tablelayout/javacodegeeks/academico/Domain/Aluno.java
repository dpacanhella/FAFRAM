package com.tablelayout.javacodegeeks.academico.Domain;

public class Aluno {
    private String qtdePresenca;
    private String nomeAluno;
    private String codigoAluno;

  public String getQtdePresenca() {
    return qtdePresenca;
  }

  public void setQtdePresenca(String qtdePresenca) {
    this.qtdePresenca = qtdePresenca;
  }

  public String getNomeAluno() {
    return nomeAluno;
  }

  public void setNomeAluno(String nomeAluno) {
    this.nomeAluno = nomeAluno;
  }

  public String getCodigoAluno() {
    return codigoAluno;
  }

  public void setCodigoAluno(String codigoAluno) {
    this.codigoAluno = codigoAluno;
  }
}
